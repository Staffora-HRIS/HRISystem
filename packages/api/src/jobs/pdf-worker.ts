/**
 * PDF Generation Worker
 *
 * Generates PDF documents for various Staffora use cases:
 * - LMS Course Completion Certificates
 * - Employment Letters
 * - Case Documentation Bundles
 * - Custom Reports
 *
 * Features:
 * - Template-based PDF generation
 * - Dynamic content rendering
 * - Document storage and retrieval
 * - Notification on completion
 */

import {
  type JobPayload,
  type JobContext,
  type ProcessorRegistration,
  JobTypes,
  StreamKeys,
} from "./base";

// =============================================================================
// Types
// =============================================================================

/**
 * PDF document types
 */
export type PdfDocumentType =
  | "certificate"
  | "employment_letter"
  | "case_bundle"
  | "offer_letter"
  | "termination_letter"
  | "salary_slip"
  | "tax_form"
  | "custom";

/**
 * Base PDF payload
 */
export interface BasePdfPayload {
  /** Document type */
  documentType: PdfDocumentType;
  /** Template to use */
  template: string;
  /** User ID to notify on completion */
  notifyUserId?: string;
}

/**
 * Certificate payload (LMS course completion)
 */
export interface CertificatePayload extends BasePdfPayload {
  documentType: "certificate";
  data: {
    /** Employee information */
    employeeId: string;
    employeeName: string;
    employeeTitle?: string;
    /** Course information */
    courseId: string;
    courseName: string;
    courseDescription?: string;
    /** Completion details */
    completionDate: string;
    score?: number;
    grade?: string;
    /** Certificate metadata */
    certificateNumber?: string;
    issuerName: string;
    issuerTitle: string;
    /** Company branding */
    companyName: string;
    companyLogo?: string;
  };
}

/**
 * Employment letter payload
 */
export interface EmploymentLetterPayload extends BasePdfPayload {
  documentType: "employment_letter";
  data: {
    /** Employee information */
    employeeId: string;
    employeeName: string;
    employeeTitle: string;
    department: string;
    /** Employment details */
    startDate: string;
    endDate?: string;
    employmentType: string;
    salary?: string;
    /** Letter specifics */
    letterType: "verification" | "reference" | "promotion" | "transfer";
    purpose?: string;
    recipientName?: string;
    recipientAddress?: string;
    /** Issuer details */
    issuerName: string;
    issuerTitle: string;
    issuerSignature?: string;
    /** Company details */
    companyName: string;
    companyAddress: string;
    companyPhone?: string;
    companyLogo?: string;
    /** Issue date */
    issueDate: string;
    referenceNumber?: string;
  };
}

/**
 * Case bundle payload (multiple documents)
 */
export interface CaseBundlePayload extends BasePdfPayload {
  documentType: "case_bundle";
  data: {
    /** Case information */
    caseId: string;
    caseNumber: string;
    caseTitle: string;
    caseType: string;
    /** Employee involved */
    employeeId: string;
    employeeName: string;
    /** Documents to include */
    documents: Array<{
      type: string;
      title: string;
      content?: string;
      attachmentId?: string;
    }>;
    /** Timeline of events */
    timeline?: Array<{
      date: string;
      event: string;
      description?: string;
      actor?: string;
    }>;
    /** Notes and summary */
    summary?: string;
    notes?: string[];
    /** Generated by */
    generatedBy: string;
    generatedAt: string;
    /** Company details */
    companyName: string;
    companyLogo?: string;
  };
}

/**
 * Union type for all PDF payloads
 */
export type PdfPayload = CertificatePayload | EmploymentLetterPayload | CaseBundlePayload;

/**
 * PDF generation result
 */
export interface PdfResult {
  /** Document ID */
  documentId: string;
  /** Download URL */
  downloadUrl: string;
  /** File size in bytes */
  fileSize: number;
  /** Number of pages */
  pageCount: number;
  /** Expiration time */
  expiresAt: Date;
}

