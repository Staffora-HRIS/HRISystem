/**
 * Process case bundle generation job
 */
async function processCaseBundle(
  payload: JobPayload<CaseBundlePayload>,
  context: JobContext
): Promise<void> {
  const { log, db, redis } = context;
  const { data, template, notifyUserId } = payload.data;

  log.info(`Generating case bundle for case ${data.caseNumber}`);

  try {
    const generator = getPdfGenerator();
    const pdfs: Buffer[] = [];

    // Generate cover page
    const coverPdf = await generator.generate(template || "case_bundle_cover", data);
    pdfs.push(coverPdf);

    // Generate each document section
    for (const doc of data.documents) {
      if (doc.content) {
        const docPdf = await generator.generate("default", {
          title: doc.title,
          content: doc.content,
        });
        pdfs.push(docPdf);
      }

      // Fetch and include attachments if available
      if (doc.attachmentId && payload.tenantId) {
        const attachment = await fetchAttachment(db, payload.tenantId, doc.attachmentId);
        if (attachment && attachment.contentType === "application/pdf") {
          // If attachment is a PDF, include it directly
          pdfs.push(attachment.content);
        } else if (attachment) {
          // For non-PDF attachments, create a placeholder page
          const placeholderPdf = await generator.generate("default", {
            title: doc.title,
            content: `[Attachment: ${attachment.filename}]\n\nFile type: ${attachment.contentType}\nSize: ${attachment.size} bytes\n\nNote: This attachment could not be embedded in the PDF bundle.`,
          });
          pdfs.push(placeholderPdf);
        }
      }
    }

    // Generate timeline section if present
    if (data.timeline && data.timeline.length > 0) {
      const timelineHtml = data.timeline
        .map((t) => `<p><strong>${t.date}</strong>: ${t.event} - ${t.description || ""}</p>`)
        .join("");

      const timelinePdf = await generator.generate("default", {
        title: "Timeline of Events",
        content: timelineHtml,
      });
      pdfs.push(timelinePdf);
    }

    // Merge all PDFs
    let finalPdf: Buffer;
    if (generator.merge && pdfs.length > 1) {
      finalPdf = await generator.merge(pdfs);
    } else {
      finalPdf = pdfs[0] || Buffer.from("%PDF-1.4 empty", "utf-8");
    }

    // Save to storage
    const storage = getDocumentStorage();
    const filename = `case_bundle_${payload.id}_${data.caseId}.pdf`;
    const filePath = await storage.save(filename, finalPdf);
    const downloadUrl = await storage.getDownloadUrl(filePath, 86400 * 30); // 30 days

    // Save document record
    await saveDocumentRecord(db, {
      id: payload.id,
      tenantId: payload.tenantId!,
      userId: data.generatedBy,
      documentType: "case_bundle",
      title: `Case Bundle: ${data.caseNumber}`,
      filePath,
      fileSize: finalPdf.length,
      metadata: {
        caseId: data.caseId,
        caseNumber: data.caseNumber,
        caseType: data.caseType,
        documentCount: data.documents.length,
      },
    });

    log.info("Case bundle generated successfully", { downloadUrl });

    // Notify user
    if (notifyUserId && payload.tenantId) {
      await notifyDocumentReady(redis, {
        tenantId: payload.tenantId,
        userId: notifyUserId,
        documentId: payload.id,
        documentType: "case_bundle",
        title: `Case Bundle: ${data.caseNumber}`,
        downloadUrl,
      });
    }
  } catch (error) {
    log.error("Case bundle generation failed", error);
    throw error;
  }
}

