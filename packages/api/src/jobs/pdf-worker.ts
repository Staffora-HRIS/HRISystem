async function fetchAttachment(
  db: import("../plugins/db").DatabaseClient,
  tenantId: string,
  attachmentId: string
): Promise<
  | {
      filename: string;
      contentType: string;
      size: number;
      content: Buffer;
    }
  | null
> {
  return await db.withSystemContext(async (tx) => {
    const rows = await tx<
      Array<{
        fileName: string;
        fileSizeBytes: number;
        mimeType: string;
        storageProvider: string;
        storageKey: string;
      }>
    >`
      SELECT
        file_name as "fileName",
        file_size_bytes as "fileSizeBytes",
        mime_type as "mimeType",
        storage_provider as "storageProvider",
        storage_key as "storageKey"
      FROM app.case_attachments
      WHERE tenant_id = ${tenantId}::uuid
        AND id = ${attachmentId}::uuid
        AND deleted_at IS NULL
      LIMIT 1
    `;

    const row = rows[0];
    if (!row) return null;

    if (row.storageProvider !== "local") {
      return null;
    }

    const fs = await import("fs/promises");
    const content = await fs.readFile(row.storageKey);

    return {
      filename: row.fileName,
      contentType: row.mimeType,
      size: row.fileSizeBytes,
      content,
    };
  });
}

/**
 * Save document record to database
 */
async function saveDocumentRecord(
  db: import("../plugins/db").DatabaseClient,
  record: {
    id: string;
    tenantId: string;
    userId: string;
    documentType: PdfDocumentType;
    title: string;
    filePath: string;
    fileSize: number;
    metadata: Record<string, unknown>;
  }
): Promise<void> {
  await db.withSystemContext(async (tx) => {
    await tx`
      INSERT INTO app.documents (
        id,
        tenant_id,
        user_id,
        document_type,
        title,
        file_path,
        file_size,
        metadata,
        created_at
      )
      VALUES (
        ${record.id}::uuid,
        ${record.tenantId}::uuid,
        ${record.userId}::uuid,
        ${record.documentType},
        ${record.title},
        ${record.filePath},
        ${record.fileSize},
        ${JSON.stringify(record.metadata)}::jsonb,
        now()
      )
    `;
  });
}

/**
 * Notify user that document is ready
 */
async function notifyDocumentReady(
  redis: import("ioredis").default,
  notification: {
    tenantId: string;
    userId: string;
    documentId: string;
    documentType: PdfDocumentType;
    title: string;
    downloadUrl: string;
  }
): Promise<void> {
  await redis.xadd(
    StreamKeys.NOTIFICATIONS,
    "*",
    "payload",
    JSON.stringify({
      id: crypto.randomUUID(),
      type: "notification.in_app",
      tenantId: notification.tenantId,
      userId: notification.userId,
      data: {
        userId: notification.userId,
        title: "Document Ready",
        message: `Your document "${notification.title}" is ready for download.`,
        type: "document_ready",
        actionUrl: notification.downloadUrl,
        actionText: "Download",
        data: {
          documentId: notification.documentId,
          documentType: notification.documentType,
        },
      },
      metadata: {
        createdAt: new Date().toISOString(),
      },
    }),
    "attempt",
    "1"
  );
}

