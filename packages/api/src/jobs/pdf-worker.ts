/**
 * Document storage interface
 */
export interface DocumentStorage {
  save(filename: string, content: Buffer): Promise<string>;
  getDownloadUrl(filePath: string, expiresIn?: number): Promise<string>;
  delete(filePath: string): Promise<void>;
}

/**
 * Local document storage
 */
export class LocalDocumentStorage implements DocumentStorage {
  private basePath: string;
  private baseUrl: string;

  constructor() {
    this.basePath = process.env["PDF_STORAGE_PATH"] || "/tmp/staffora-documents";
    this.baseUrl = process.env["PDF_BASE_URL"] || "http://localhost:3000/api/documents";
  }

  async save(filename: string, content: Buffer): Promise<string> {
    const fs = await import("fs/promises");
    const path = await import("path");

    await fs.mkdir(this.basePath, { recursive: true });
    const filePath = path.join(this.basePath, filename);
    await fs.writeFile(filePath, content);

    return filePath;
  }

  async getDownloadUrl(filePath: string, _expiresIn?: number): Promise<string> {
    const path = await import("path");
    const filename = path.basename(filePath);
    return `${this.baseUrl}/${filename}`;
  }

  async delete(filePath: string): Promise<void> {
    const fs = await import("fs/promises");
    try {
      await fs.unlink(filePath);
    } catch (error) {
      if ((error as NodeJS.ErrnoException).code !== "ENOENT") {
        throw error;
      }
    }
  }
}

/**
 * Get document storage
 */
function getDocumentStorage(): DocumentStorage {
  return new LocalDocumentStorage();
}

