/**
 * Process certificate generation job
 */
async function processCertificate(
  payload: JobPayload<CertificatePayload>,
  context: JobContext
): Promise<void> {
  const { log, db, redis } = context;
  const { data, template, notifyUserId } = payload.data;

  log.info(`Generating certificate for ${data.employeeName}`);

  try {
    // Generate PDF
    const generator = getPdfGenerator();
    const pdfBuffer = await generator.generate(template || "certificate", data);

    // Save to storage
    const storage = getDocumentStorage();
    const filename = `certificate_${payload.id}_${data.employeeId}.pdf`;
    const filePath = await storage.save(filename, pdfBuffer);
    const downloadUrl = await storage.getDownloadUrl(filePath, 86400 * 7);

    // Save document record
    await saveDocumentRecord(db, {
      id: payload.id,
      tenantId: payload.tenantId!,
      userId: data.employeeId,
      documentType: "certificate",
      title: `Certificate: ${data.courseName}`,
      filePath,
      fileSize: pdfBuffer.length,
      metadata: {
        courseId: data.courseId,
        courseName: data.courseName,
        completionDate: data.completionDate,
        certificateNumber: data.certificateNumber,
      },
    });

    log.info("Certificate generated successfully", { downloadUrl });

    // Notify user
    if (notifyUserId && payload.tenantId) {
      await notifyDocumentReady(redis, {
        tenantId: payload.tenantId,
        userId: notifyUserId,
        documentId: payload.id,
        documentType: "certificate",
        title: `Certificate: ${data.courseName}`,
        downloadUrl,
      });
    }
  } catch (error) {
    log.error("Certificate generation failed", error);
    throw error;
  }
}

