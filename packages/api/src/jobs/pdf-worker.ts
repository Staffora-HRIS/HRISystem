/**
 * PDF generator interface
 * Implement this with your preferred PDF library
 */
export interface PdfGenerator {
  /** Generate a PDF from template and data */
  generate(template: string, data: Record<string, unknown>): Promise<Buffer>;
  /** Merge multiple PDFs into one */
  merge?(pdfs: Buffer[]): Promise<Buffer>;
}

/**
 * PDF generator using pdf-lib for programmatic PDF generation
 */
export class HtmlPdfGenerator implements PdfGenerator {
  async generate(template: string, data: Record<string, unknown>): Promise<Buffer> {
    const { PDFDocument, rgb, StandardFonts } = await import("pdf-lib");

    const pdfDoc = await PDFDocument.create();
    const helveticaFont = await pdfDoc.embedFont(StandardFonts.Helvetica);
    const helveticaBold = await pdfDoc.embedFont(StandardFonts.HelveticaBold);
    const timesRoman = await pdfDoc.embedFont(StandardFonts.TimesRoman);
    const timesRomanBold = await pdfDoc.embedFont(StandardFonts.TimesRomanBold);

    const page = pdfDoc.addPage([612, 792]); // Letter size
    const { width, height } = page.getSize();
    const margin = 50;

    // Colors
    const primaryColor = rgb(0.1, 0.21, 0.36); // #1a365d
    const secondaryColor = rgb(0.18, 0.22, 0.28); // #2d3748
    const goldColor = rgb(0.79, 0.64, 0.15); // #c9a227
    const grayColor = rgb(0.44, 0.52, 0.59); // #718096

    let y = height - margin;

    switch (template) {
      case "certificate": {
        // Certificate template
        // Border
        page.drawRectangle({
          x: margin - 10,
          y: margin - 10,
          width: width - 2 * margin + 20,
          height: height - 2 * margin + 20,
          borderColor: goldColor,
          borderWidth: 3,
        });

        // Company name
        const companyName = String(data.companyName || "Company");
        const companyWidth = timesRomanBold.widthOfTextAtSize(companyName, 20);
        page.drawText(companyName, {
          x: (width - companyWidth) / 2,
          y: y - 40,
          size: 20,
          font: timesRomanBold,
          color: secondaryColor,
        });
        y -= 80;

        // Certificate title
        const title = "Certificate of Completion";
        const titleWidth = timesRomanBold.widthOfTextAtSize(title, 32);
        page.drawText(title, {
          x: (width - titleWidth) / 2,
          y,
          size: 32,
          font: timesRomanBold,
          color: primaryColor,
        });
        y -= 60;

        // "This certifies that"
        const certifiesText = "This certifies that";
        const certifiesWidth = timesRoman.widthOfTextAtSize(certifiesText, 14);
        page.drawText(certifiesText, {
          x: (width - certifiesWidth) / 2,
          y,
          size: 14,
          font: timesRoman,
          color: grayColor,
        });
        y -= 50;

        // Employee name
        const employeeName = String(data.employeeName || "Employee");
        const nameWidth = timesRomanBold.widthOfTextAtSize(employeeName, 28);
        page.drawText(employeeName, {
          x: (width - nameWidth) / 2,
          y,
          size: 28,
          font: timesRomanBold,
          color: primaryColor,
        });
        y -= 10;
        // Underline
        page.drawLine({
          start: { x: (width - nameWidth) / 2 - 20, y },
          end: { x: (width + nameWidth) / 2 + 20, y },
          thickness: 2,
          color: goldColor,
        });
        y -= 50;

        // "has successfully completed"
        const completedText = "has successfully completed";
        const completedWidth = timesRoman.widthOfTextAtSize(completedText, 14);
        page.drawText(completedText, {
          x: (width - completedWidth) / 2,
          y,
          size: 14,
          font: timesRoman,
          color: grayColor,
        });
        y -= 50;

        // Course name
        const courseName = String(data.courseName || "Course");
        const courseWidth = timesRomanBold.widthOfTextAtSize(courseName, 22);
        page.drawText(courseName, {
          x: (width - courseWidth) / 2,
          y,
          size: 22,
          font: timesRomanBold,
          color: secondaryColor,
        });
        y -= 60;

        // Completion date
        const dateText = `Completed on ${data.completionDate || "Date"}`;
        const dateWidth = timesRoman.widthOfTextAtSize(dateText, 12);
        page.drawText(dateText, {
          x: (width - dateWidth) / 2,
          y,
          size: 12,
          font: timesRoman,
          color: grayColor,
        });
        y -= 80;

        // Signature line
        const sigLineWidth = 200;
        page.drawLine({
          start: { x: (width - sigLineWidth) / 2, y },
          end: { x: (width + sigLineWidth) / 2, y },
          thickness: 1,
          color: secondaryColor,
        });
        y -= 20;

        // Issuer name and title
        const issuerInfo = `${data.issuerName || "Issuer"}, ${data.issuerTitle || "Title"}`;
        const issuerWidth = timesRoman.widthOfTextAtSize(issuerInfo, 12);
        page.drawText(issuerInfo, {
          x: (width - issuerWidth) / 2,
          y,
          size: 12,
          font: timesRoman,
          color: secondaryColor,
        });
        break;
      }

      case "employment_letter": {
        // Letterhead
        const companyName = String(data.companyName || "Company");
        page.drawText(companyName, {
          x: margin,
          y,
          size: 20,
          font: helveticaBold,
          color: primaryColor,
        });
        y -= 20;

        if (data.companyAddress) {
          page.drawText(String(data.companyAddress), {
            x: margin,
            y,
            size: 10,
            font: helveticaFont,
            color: grayColor,
          });
          y -= 15;
        }

        // Horizontal line
        page.drawLine({
          start: { x: margin, y },
          end: { x: width - margin, y },
          thickness: 2,
          color: secondaryColor,
        });
        y -= 40;

        // Date and reference
        page.drawText(`Date: ${data.issueDate || "Date"}`, {
          x: width - margin - 150,
          y,
          size: 11,
          font: helveticaFont,
          color: secondaryColor,
        });
        y -= 15;

        if (data.referenceNumber) {
          page.drawText(`Ref: ${data.referenceNumber}`, {
            x: width - margin - 150,
            y,
            size: 11,
            font: helveticaFont,
            color: secondaryColor,
          });
        }
        y -= 40;

        // Recipient
        page.drawText("To Whom It May Concern", {
          x: margin,
          y,
          size: 11,
          font: helveticaFont,
          color: secondaryColor,
        });
        y -= 30;

        // Subject
        const subject = `Subject: Employment Verification for ${data.employeeName || "Employee"}`;
        page.drawText(subject, {
          x: margin,
          y,
          size: 11,
          font: helveticaBold,
          color: secondaryColor,
        });
        y -= 40;

        // Body paragraphs
        const paragraphs = [
          `This letter confirms that ${data.employeeName || "Employee"} is employed with ${companyName} as a ${data.employeeTitle || "Position"} in the ${data.department || "Department"} department.`,
          `Employment Start Date: ${data.startDate || "Date"}`,
          `Employment Type: ${data.employmentType || "Type"}`,
          `This letter is issued at the request of the employee${data.purpose ? ` for ${data.purpose}` : ""}.`,
          "If you require any additional information, please contact our HR department.",
        ];

        for (const para of paragraphs) {
          // Word wrap
          const words = para.split(" ");
          let line = "";
          const maxWidth = width - 2 * margin;

          for (const word of words) {
            const testLine = line + (line ? " " : "") + word;
            const testWidth = helveticaFont.widthOfTextAtSize(testLine, 11);

            if (testWidth > maxWidth && line) {
              page.drawText(line, { x: margin, y, size: 11, font: helveticaFont, color: secondaryColor });
              y -= 18;
              line = word;
            } else {
              line = testLine;
            }
          }
          if (line) {
            page.drawText(line, { x: margin, y, size: 11, font: helveticaFont, color: secondaryColor });
            y -= 30;
          }
        }
        y -= 20;

        // Signature
        page.drawText("Sincerely,", {
          x: margin,
          y,
          size: 11,
          font: helveticaFont,
          color: secondaryColor,
        });
        y -= 60;

        page.drawText(String(data.issuerName || "Name"), {
          x: margin,
          y,
          size: 11,
          font: helveticaBold,
          color: secondaryColor,
        });
        y -= 15;
        page.drawText(String(data.issuerTitle || "Title"), {
          x: margin,
          y,
          size: 11,
          font: helveticaFont,
          color: secondaryColor,
        });
        break;
      }

      case "case_bundle_cover": {
        // Cover page for case bundle
        y = height / 2 + 150;

        const companyName = String(data.companyName || "Company");
        const companyWidth = helveticaBold.widthOfTextAtSize(companyName, 24);
        page.drawText(companyName, {
          x: (width - companyWidth) / 2,
          y,
          size: 24,
          font: helveticaBold,
          color: primaryColor,
        });
        y -= 80;

        const title = "Case Documentation Bundle";
        const titleWidth = helveticaBold.widthOfTextAtSize(title, 28);
        page.drawText(title, {
          x: (width - titleWidth) / 2,
          y,
          size: 28,
          font: helveticaBold,
          color: secondaryColor,
        });
        y -= 40;

        const caseTitle = String(data.caseTitle || "Case");
        const caseTitleWidth = helveticaFont.widthOfTextAtSize(caseTitle, 18);
        page.drawText(caseTitle, {
          x: (width - caseTitleWidth) / 2,
          y,
          size: 18,
          font: helveticaFont,
          color: grayColor,
        });
        y -= 80;

        // Details
        const details = [
          ["Case Number:", String(data.caseNumber || "")],
          ["Case Type:", String(data.caseType || "")],
          ["Employee:", String(data.employeeName || "")],
          ["Generated By:", String(data.generatedBy || "")],
          ["Generated At:", String(data.generatedAt || "")],
        ];

        for (const [label, value] of details) {
          page.drawText(label, { x: margin + 100, y, size: 11, font: helveticaBold, color: secondaryColor });
          page.drawText(value, { x: margin + 220, y, size: 11, font: helveticaFont, color: secondaryColor });
          y -= 20;
        }
        y -= 80;

        // Confidential notice
        const confText = "CONFIDENTIAL - FOR AUTHORIZED PERSONNEL ONLY";
        const confWidth = helveticaBold.widthOfTextAtSize(confText, 14);
        page.drawText(confText, {
          x: (width - confWidth) / 2,
          y,
          size: 14,
          font: helveticaBold,
          color: rgb(0.9, 0.24, 0.24), // Red
        });
        break;
      }

      default: {
        // Default simple document
        const title = String(data.title || "Document");
        page.drawText(title, {
          x: margin,
          y,
          size: 18,
          font: helveticaBold,
          color: primaryColor,
        });
        y -= 40;

        const content = String(data.content || "");
        // Simple word wrap
        const words = content.split(" ");
        let line = "";
        const maxWidth = width - 2 * margin;

        for (const word of words) {
          const testLine = line + (line ? " " : "") + word;
          const testWidth = helveticaFont.widthOfTextAtSize(testLine, 11);

          if (testWidth > maxWidth && line) {
            page.drawText(line, { x: margin, y, size: 11, font: helveticaFont, color: secondaryColor });
            y -= 16;
            line = word;
          } else {
            line = testLine;
          }
        }
        if (line) {
          page.drawText(line, { x: margin, y, size: 11, font: helveticaFont, color: secondaryColor });
        }
        break;
      }
    }

    const pdfBytes = await pdfDoc.save();
    return Buffer.from(pdfBytes);
  }

  async merge(pdfs: Buffer[]): Promise<Buffer> {
    const { PDFDocument } = await import("pdf-lib");

    const mergedPdf = await PDFDocument.create();

    for (const pdfBuffer of pdfs) {
      try {
        const pdf = await PDFDocument.load(pdfBuffer);
        const pages = await mergedPdf.copyPages(pdf, pdf.getPageIndices());
        pages.forEach((page) => mergedPdf.addPage(page));
      } catch (error) {
        console.error("[HtmlPdfGenerator] Failed to merge PDF:", error);
        // Skip invalid PDFs
      }
    }

    const pdfBytes = await mergedPdf.save();
    return Buffer.from(pdfBytes);
  }
}

